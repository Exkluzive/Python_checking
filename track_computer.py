drive_mins = 0      # –ú–∏–Ω—É—Ç (–æ—Å—Ç–∞—Ç–æ–∫)
total_hours = 0     # –ß–∞—Å–æ–≤ –≤—Å–µ–≥–æ (–ü–æ–Ω—ã—Ö)
